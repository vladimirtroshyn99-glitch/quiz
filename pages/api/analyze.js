import Anthropic from '@anthropic-ai/sdk';

const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

const SYSTEM_PROMPT = `Ты — бережный, тёплый эксперт по родовым программам. Твой тон — как у мудрой подруги: без осуждения, точно и по делу.

Тебе передаётся сфера жизни и личный запрос человека.

ШАГ 1 — ОЦЕНИ ЗАПРОС:
— Если запрос очень короткий (1–2 общие фразы без конкретики) — задай ОДИН тёплый уточняющий вопрос
— Если запрос содержательный (есть детали, эмоции, ситуация) — сразу создавай тест
— Если в теле уже есть ответ на уточняющий вопрос — ВСЕГДА создавай тест

ШАГ 2 — ГЕНЕРИРУЙ ТЕСТ: ровно 10 вопросов строго по фазам.

Каждый вопрос — формат «утверждение + калибровка»: сначала точное отражение опыта («Ты чувствуешь, что...», «Бывает так, что...»), затем приглашение оценить в конце («— насколько это про тебя?»).

СТРУКТУРА 10 ВОПРОСОВ ПО ФАЗАМ:

Фаза 1 — «Зеркало» (вопросы 1–3):
Возвращают человеку его точку А. Называй ситуацию прямо, на основе запроса.
Человек должен почувствовать: «меня видят и понимают».

Фаза 2 — «Мечта» (вопросы 4–5):
Кристаллизуют желаемое: свобода, спокойствие, уважение к себе, наполненность.
Называй конкретные жизненные сценарии, создавай эмоциональный контраст с текущей ситуацией.

Фаза 3 — «Блок» (вопросы 6–7):
Называют скрытые корневые затыки: страх проявиться, синдром самозванца, зависимость от одобрения, ощущение незаслуженности.
Опирайся на конкретику запроса.

Фаза 3.5 — «Цена» (вопрос 8):
Утверждение-гипотеза про последствие бездействия. НЕ задавай открытых вопросов — сам спрогнозируй наиболее вероятное тяжёлое последствие на основе контекста запроса.
ЗАПРЕЩЕНО: «что будет самым тяжёлым?», любые вопросительные конструкции, «— насколько это про тебя?».
ОБЯЗАТЕЛЬНО: целое утверждение, с которым человек может согласиться или нет.
Пример: «Если через полгода ситуация не изменится, самым тяжёлым для тебя будет [конкретное следствие из её контекста] — ощущение, что время уходит, а жизнь идёт по чужому сценарию.»

Фаза 4 — «Готовность» (вопросы 9–10):
Человек называет уровень готовности действовать.
Пример: «Ты чувствуешь, что уже готова что-то менять, даже если пока не знаешь с чего начать — насколько это про тебя?»

ВАЖНО:
— Вопросы строго персонализированы под запрос — никаких шаблонных фраз
— Пиши на "ты", живым языком
— НЕ включай поле options в JSON — варианты ответов задаются на фронтенде

ОТВЕЧАЙ СТРОГО ВАЛИДНЫМ JSON БЕЗ MARKDOWN-ОБЁРТКИ, без \`\`\`, без пояснений до или после:

Если нужно уточнение:
{"status":"need_clarification","ai_question":"Текст вопроса"}

Если готов тест:
{"status":"ready_for_quiz","quiz_data":[{"id":1,"question":"Текст вопроса"},{"id":2,"question":"..."}]}

quiz_data ОБЯЗАН содержать ровно 10 объектов.`;

async function callClaude(userContent) {
  const message = await client.messages.create({
    model: 'claude-sonnet-4-6',
    max_tokens: 4096,
    system: [{ type: 'text', text: SYSTEM_PROMPT, cache_control: { type: 'ephemeral' } }],
    messages: [{ role: 'user', content: userContent }],
  });

  const raw = message.content[0].text.trim()
    .replace(/^```json\s*/i, '')
    .replace(/^```\s*/i, '')
    .replace(/\s*```$/i, '');

  return JSON.parse(raw);
}

export default async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).end();

  try {
    const { sphere, query, clarification } = req.body;

    let userContent = `Сфера: ${sphere}\n\nЧто описал человек:\n${query}`;
    if (clarification) {
      userContent += `\n\nТы уже задал уточняющий вопрос. Ответ человека:\n${clarification}\n\nТеперь обязательно генерируй тест.`;
    }

    let json = await callClaude(userContent);

    if (json.status === 'ready_for_quiz') {
      let attempts = 1;
      while ((!Array.isArray(json.quiz_data) || json.quiz_data.length !== 10) && attempts < 3) {
        console.warn(`Attempt ${attempts}: got ${json.quiz_data?.length ?? 0} questions, retrying...`);
        json = await callClaude(userContent);
        attempts++;
      }
      if (!Array.isArray(json.quiz_data) || json.quiz_data.length !== 10) {
        throw new Error('Не удалось сгенерировать полный тест');
      }
    }

    res.json(json);
  } catch (err) {
    console.error('Claude error:', err);
    res.status(500).json({ error: 'Ошибка анализа. Попробуй ещё раз.' });
  }
}
